        Covid-19, circonstance imprévue et modification du contrat : TA Paris 15 juin 2024,
  req. n° 2412367
<br><br>
  Par un jugement du 15 juin 2024, soit près de quatre ans après la crise sanitaire, le tribunal
  administratif de Paris a qualifié pour la première fois la pandémie du Covid-19 de
  « circonstance imprévue » au sens du Code de la commande publique justifiant la signature
  d'un avenant au contrat de concession sans mise en concurrence.
<br><br>
  Ce faisant, le tribunal a donné une grille d'appréciation de la légalité des modifications
  contractuelles rendues nécessaires par de telles circonstances.
<br><br>
  Les circonstances imprévues sont définies par le Code de la commande publique comme
  « des circonstances qu'un acheteur ou une autorité concédante diligent ne pouvait pas
  prévoir ».
<br><br>
  Pour autant, ce code ne précise ni quelles sont les circonstances qui peuvent, ou pas, être
  prévues par un acheteur diligent, ni ce qu'est un acheteur diligent.
<br><br>
  Pour les définir, on peut se référer au considérant 109 de la directive 2014/24/UE du
  26 février 2014 duquel résulte que ce sont des circonstances extérieures qu'un acheteur,
  « bien qu'ayant fait preuve d'une diligence raisonnable lors de la préparation du contrat
  initial, n'aurait pu prévoir, compte tenu des moyens à sa disposition, de la nature et des
  caractéristiques du projet particulier, des bonnes pratiques du secteur et de la nécessité de
  mettre en adéquation les ressources consacrées à la préparation de l'attribution du marché
  et la valeur prévisible de celui-ci ».
<br><br>
  Le débat tenant à considérer que les « circonstances imprévues » doivent être distinguées
  des « circonstances imprévisibles » est donc a priori définitivement clos, le juge administratif
  utilisant face à une même situation, tantôt la première expression, tantôt la seconde.
<br><br>
  En effet sous l'empire du Code des marchés publics, ces deux circonstances pouvaient se
  distinguer par le degré de probabilité de leur survenance, selon les pratiques constatées
  dans un secteur d'activité donné.
<br><br>
  Si elles devaient toujours être des phénomènes extérieurs aux parties et irrésistibles, les
  circonstances imprévisibles étaient celles qui déjouaient toutes les prévisions des parties
  alors que les circonstances imprévues étaient celles qui excédaient seulement les
  vicissitudes de la vie économique .
  Désormais, les circonstances imprévues sont définies comme celles qui « ont dépassé les
  limites ayant pu raisonnablement être envisagées par les parties lors de la passation du
  contrat » , actant ainsi l'assimilation de l'imprévu à l'imprévisible.
  Le récent jugement du tribunal administratif de Paris vient donc qualifier la pandémie de
  Covid-19 de circonstance que l'autorité concédante diligente ne pouvait pas prévoir, justifiant
  la signature d'un avenant au contrat sans nouvelle procédure de mise en concurrence.
<br><br>
